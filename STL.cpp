#include <iostream>        // Подключает библиотеку для ввода‑вывода (std::cout, std::endl)
#include <map>           // Подключает контейнер std::map (ассоциативный массив «ключ‑значение»)
#include <set>           // Подключает контейнер std::set (множество уникальных элементов)
#include <sstream>      // Подключает строковые потоки (std::istringstream для разбора строк)
#include <string>       // Подключает тип std::string (строки)
#include <vector>       // Подключает контейнер std::vector (динамический массив)

int main() {             // Начало функции main — точка входа в программу
    // 1. Частота элементов (подсчёт частоты чисел через map)
    std::vector<int> numbers = { 1, 2, 3, 2, 1, 3, 2, 4 };
    // Создаём вектор целых чисел с заданными значениями

    std::map<int, int> frequency;
    // Создаём map: ключ — число (int), значение — его частота (int)

    for (int num : numbers) {
        // Цикл по всем элементам вектора numbers; на каждой итерации num — текущий элемент
        frequency[num]++;
        // Если ключа num нет в map, создаётся со значением 0, затем увеличивается на 1
    }

    std::cout << "1. Частота элементов:\n";
    // Выводим заголовок раздела

    for (const auto& pair : frequency) {
        // Цикл по всем парам «ключ‑значение» в map; pair — текущая пара
        std::cout << "Число " << pair.first << " встречается "
            << pair.second << " раз(а)\n";
        // pair.first — число (ключ), pair.second — его частота (значение); выводим строку
    }
    std::cout << std::endl;
    // Переводим строку (два перевода строки: \n + std::endl)

    // 2. Самое частое число (найти элемент с максимальной частотой)
    int mostFrequent = 0;
    // Переменная для хранения самого частого числа (инициализируем 0)
    int maxCount = 0;
    // Переменная для хранения максимальной частоты (инициализируем 0)

    for (const auto& pair : frequency) {
        // Цикл по всем парам в map frequency
        if (pair.second > maxCount) {
            // Если частота текущего числа больше текущей максимальной
            maxCount = pair.second;
            // Обновляем максимальную частоту
            mostFrequent = pair.first;
            // Сохраняем само число
        }
    }

    std::cout << "2. Самое частое число: " << mostFrequent
        << " (встречается " << maxCount << " раз)\n";
    // Выводим результат: самое частое число и его частоту
    std::cout << std::endl;
    // Переводим строку

    // 3. Проверка существования (наличие ключа в map)
    std::map<std::string, int> ages = { {"Alice", 25}, {"Bob", 30}, {"Charlie", 35} };
    // Создаём map: ключ — имя (std::string), значение — возраст (int)
    std::string keyToCheck = "Alice";
    // Ключ, который будем искать в map

    std::cout << "3. Проверка существования ключа '" << keyToCheck << "':\n";
    // Выводим заголовок с проверяемым ключом

    if (ages.find(keyToCheck) != ages.end()) {
        // find() возвращает итератор на элемент или end(), если не найден
        std::cout << "Ключ найден (через find())\n";
        // Выводим сообщение, если ключ найден
    }
    else {
        std::cout << "Ключ не найден\n";
        // Выводим сообщение, если ключ не найден
    }

    if (ages.count(keyToCheck)) {
        // count() возвращает 1, если ключ есть, 0 — если нет
        std::cout << "Ключ существует (через count())\n";
        // Выводим сообщение, если count() вернул 1 (true)
    }

#ifdef __cpp_lib_contains
    // Условная компиляция: код выполнится, только если поддерживается C++20
    if (ages.contains(keyToCheck)) {
        // contains() возвращает true, если ключ есть в map (C++20)
        std::cout << "Ключ присутствует (через contains())\n";
        // Выводим сообщение, если ключ найден через contains()
    }
#endif
    std::cout << std::endl;
    // Переводим строку

    // 4. Уникальные слова (из строки получить уникальные слова через set)
    std::string text = "apple banana apple orange banana grape";
    // Исходная строка с повторяющимися словами
    std::istringstream iss(text);
    // Создаём входной строковый поток для разбора строки на слова
    std::set<std::string> uniqueWords;
    // Создаём set для хранения уникальных слов (автоматически удаляет дубликаты)
    std::string word;
    // Временная переменная для хранения текущего слова при чтении из потока

    while (iss >> word) {
        // Читаем слова из потока iss, пока они есть; каждое слово записываем в word
        uniqueWords.insert(word);
        // Добавляем слово в set; если оно уже есть, вставка игнорируется
    }

    std::cout << "4. Уникальные слова:\n";
    // Выводим заголовок раздела
    for (const auto& w : uniqueWords) {
        // Цикл по всем словам в set (уже отсортированным)
        std::cout << w << "\n";
        // Выводим каждое уникальное слово с переводом строки
    }
    std::cout << std::endl;
    // Переводим строку

    // 5. Сортировка слов (подсчитать слова и вывести по алфавиту через map)
    std::string text2 = "banana apple orange apple banana grape";
    // Вторая строка с словами (может отличаться от text)
    std::istringstream iss2(text2);
    // Создаём второй строковый поток для разбора text2
    std::map<std::string, int> wordCount;
    // Создаём map для подсчёта частот слов: ключ — слово, значение — частота

    while (iss2 >> word) {
        // Читаем слова из iss2, пока они есть
        wordCount[word]++;
        // Для каждого слова увеличиваем счётчик в map: если слова нет, создаётся с 0, затем +1
    }

    std::cout << "5. Слова по алфавиту с частотами:\n";
    // Выводим заголовок раздела
    for (const auto& pair : wordCount) {
        // Цикл по парам «слово — частота» в map (ключи отсортированы по алфавиту)
        std::cout << pair.first << ": " << pair.second << " раз(а)\n";
        // pair.first — слово, pair.second — частота; выводим в формате «слово: частота»
    }

    return 0;  // Завершаем программу с кодом 0 (успешное выполнение)
}
